    input wire en,             // ?????? ??????? (Enable)
    output reg [3:0] out0, out1, out2, out3, out4, out5, out6, out7 // 8 ??????? ?? 4 ????
);

    always @(*) begin
        // ?? ????????????? ?? ???? ??????? ????, ??? ???????? ????????? ??????? (latches)
        out0 = 4'b0000;
        out1 = 4'b0000;
        out2 = 4'b0000;
        out3 = 4'b0000;
        out4 = 4'b0000;
        out5 = 4'b0000;
        out6 = 4'b0000;
        out7 = 4'b0000;

        // ???? ?????? ?????????, ????????? ???? ?? ??????? ?????
        if (en) begin
            case (sel)
                3'b000: out0 = data_in;
